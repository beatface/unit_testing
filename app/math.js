function sum(one, two) {
  return one + two;
}

function difference(one, two) {
	return one - two;
}

function product(one, two) {
	return one * two;
}

function quotient(one, two) {
	return one / two;
}

function squared(num) {
	return num * num;
}

module.exports = { sum, difference, product, quotient, squared };